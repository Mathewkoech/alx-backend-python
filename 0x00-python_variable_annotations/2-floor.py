#!/usr/bin/env python3
"""
Module takes a float n as argument and returns the floor of the float.
"""


import math


def floor(n: float) -> int:
    """
    Returns the floor of the float number.

    Parameters:
    n (float): The float number to find the floor of.

    Returns:
    int: The floor of n.
    """
    return math.floor(n)
